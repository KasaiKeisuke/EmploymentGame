#include "GameManager.h"

//*******************************************************************************************************************************************
// コンストラクタ
//*******************************************************************************************************************************************
CGameManager::CGameManager()
{
	m_nScore = 0;
	m_nTime = 0;
}

//*******************************************************************************************************************************************
// デストラクタ
//*******************************************************************************************************************************************
CGameManager::~CGameManager()
{
	m_nScore = 0;
	m_nTime = 0;
}

//*******************************************************************************************************************************************
// 初期化処理
//*******************************************************************************************************************************************
HRESULT CGameManager::Init()
{
	m_nScore = 0;
	m_nTime = 180;

	return S_OK;
}

//*******************************************************************************************************************************************
// 終了処理
//*******************************************************************************************************************************************
void CGameManager::Uninit()
{
	m_nScore = 0;
	m_nTime = 180;
}

//*******************************************************************************************************************************************
// 更新処理
//*******************************************************************************************************************************************
void CGameManager::Update()
{

}

//*******************************************************************************************************************************************
// 制限時間設定処理
//*******************************************************************************************************************************************
void CGameManager::SetTimer(int nTime)
{
	m_nTime = nTime;
}

//*******************************************************************************************************************************************
// 制限時間取得処理
//*******************************************************************************************************************************************
int CGameManager::GetTimer()
{
	return m_nTime;
}

//*******************************************************************************************************************************************
// スコア設定処理
//*******************************************************************************************************************************************
void CGameManager::SetScore(int nScore)
{
	m_nScore = nScore;
}

//*******************************************************************************************************************************************
// スコア取得処理
//*******************************************************************************************************************************************
int CGameManager::GetScore()
{
	return m_nScore;
}
